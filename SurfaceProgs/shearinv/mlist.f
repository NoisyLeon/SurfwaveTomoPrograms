      SUBROUTINE  MLIST
      REAL*8  RAD,TMASS,GS,RHO0,GA
      REAL*8  D,H,RHO,ELAMB,EMU,GR,XI,PHI,ETA
      COMMON/MODEL /NLAY,ISO,LAME,NDIV,DMAX,ILAY,
     1              RAD,TMASS,GS,RHO0,GA,
     2              HI(500),RHOI(500),VPI(500),VSI(500),NAME(20),
     3              XII(500),PHII(500),ETAI(500),
     4              D(1000),H(1000),RHO(1000),ELAMB(1000),EMU(1000),
     5              GR(1000),XI(1000),PHI(1000),ETA(1000)
C
C PURPOSE - TO PRINT OUT MODEL
C
      WRITE(6,1001)
 1001 FORMAT(1H0,3X,5HDEPTH,8X,3HRHO,8X,2HVP,9X,2HVS,9X,1HG,10X,2HXI,
     1       8X,3HPHI,7X,3HETA)
      DO  1500  I=1,NLAY
      DC=D(I)
      S=EMU(I)/RHO(I)
      P=ELAMB(I)/RHO(I)+2.0*S
      P=SQRT(P)
      S=SQRT(S)
      WRITE(6,1501)  DC,RHO(I),P,S,GR(I),XI(I),PHI(I),ETA(I)
 1501 FORMAT(1H ,F10.5,3F11.5,5PF11.3,0PF11.5,2F10.5)
 1500 CONTINUE
C 
      pause  
      RETURN
      END
